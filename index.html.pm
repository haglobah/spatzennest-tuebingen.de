  ◊section["Informationen zur Kinderanmeldung"]{
    ◊par{
      Die Anmeldung ist nur für einen der beiden Freizeitabschnitte möglich (also entweder für die 1. Freizeit oder für die 2. Freizeit). In jedem Freizeitabschnitt gibt es Plätze für 330 Kinder, wobei die 2. Freizeit in den vergangenen Jahren etwas weniger schnell ausbebucht war.
    }
    ◊par{
      Die Anmeldung zu den Freizeiten ist grundsätzlich ◊bold{nur online} möglich. Ab dem ◊bold{1. Februar} wird der Anmeldelink auf unserer Homepage freigeschaltet. Sollten bis zum 7. Februar mehr Kinder angemeldet sein, als es Plätze hat, wird zwischen allen Kinderanmeldungen vom 1. bis zum 7. Februar per Los entschieden.
      Sollte ihr Kind nicht ausgelost werden, wird es auf die Warteliste gesetzt.
    }
    ◊par{
      Die gestaffelten Elternbeiträge für Ihre Anmeldung entnehmen Sie bitte der Beitragstabelle auf dieser Seite weiter unten.
    }
    ◊par{
      Um den Geschwisterkinder-Rabatt zu erhalten, müssen Geschwisterkinder gemeinsam (nicht einzeln) angemeldt werden. Ein Rabatt kann nicht nachträglich gewährt werden. Zudem gilt der Geschwisterrabatt nicht abschnittsübergreifend (sondern jeweils nur für einen Freizeitabschnitt).
    }
    ◊par{
      Die Teilnahmebestätigung erhalten Sie bis Mitte, spätestens aber bis Ende April; die Rechnung erhalten Sie bis Ende Mai, spätestens aber bis Mitte Juni. TN-Bestätigung und Rechnung werden per E-Mail verschickt.
    }
    ◊par{
      Bitte haken sie erst ◊bold{nach dem Ende} des jeweiligen Zeitraumes nach, dass sie noch keine Teilnahmebestätigung/Rechnung erhalten haben. Zusätzlich: Schauen sie zuerst im Spam-Ordner nach, bevor sie uns eine Rückfrage schicken.
    }
    ◊par{
      Die Teilnahme ihres Kindes ◊bold{ist erst nach Zahlungseingang sicher}.
    }
    ◊par{
